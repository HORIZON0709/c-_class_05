CStudent::CStudent() : CHuman(TYPE::STUDENT),
m_nAbility(0)
{
	//メンバ変数をクリア
	//m_nAbility = 0;
}

//===================================================
//デストラクタ
//===================================================
CStudent::~CStudent()
{
	/* 処理無し */
}

//===================================================
//初期化
//===================================================
void CStudent::Init()
{
	//メンバ変数を初期化
	m_nAbility = 0;
}

//===================================================
//入力
//===================================================
void CStudent::Input()
{
	printf("\n 名前 > ");
	scanf("%s", &m_aName[0]);

	printf(" 体力 > ");
	scanf("%d", &m_nLife);

	printf(" 学力 > ");
	scanf("%d", &m_nAbility);
}

//===================================================
//授業
//===================================================
void CStudent::Class()
{
	//メッセージ
	printf("\n %sの体力Down! テンションUp!", &m_aName[0]);

	//各パラメータの増減
	m_nLife--;
	m_nAbility++;
}

//===================================================
//休憩
//===================================================
void CStudent::Rest()
{
	//メッセージ
	printf("\n %sの体力Up!", &m_aName[0]);

	//パラメータの増加
	m_nLife++;
}

//===================================================
//出力
//===================================================
void CStudent::Output()
{
	printf("\n 名前 : [ %s ]", &m_aName[0]);	//名前
	printf("\n 体力 : [ %d ]", m_nLife);		//体力
	printf("\n 学力 : [ %d ]", m_nAbility);		//学力
}

