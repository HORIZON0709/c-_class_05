#include "teacher.h"

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

//********************************
//静的メンバ変数
//********************************

//===================================================
//コンストラクタ
//===================================================
CTeacher::CTeacher() : CHuman(TYPE::TEACHER),
m_nTension(0)
{
	
}

//===================================================
//デストラクタ
//===================================================
CTeacher::~CTeacher()
{
	/* 処理無し */
}

//===================================================
//初期化
//===================================================
void CTeacher::Init()
{
	//メンバ変数を初期化
	strcpy(&m_aName[0], "ゆだちゃん");
	m_nLife = 30;
	m_nTension = 30;
}

//===================================================
//授業
//===================================================
void CTeacher::Class()
{
	//メッセージ
	printf("\n %s先生の体力Down! テンションUp!", &m_aName[0]);

	//各パラメータの増減
	m_nLife--;
	m_nTension++;
}

//===================================================
//休憩
//===================================================
void CTeacher::Rest()
{
	//メッセージ
	printf("\n %s先生の体力Up!", &m_aName[0]);

	//パラメータの増加
	m_nLife++;
}

//===================================================
//出力
//===================================================
void CTeacher::Output()
{
	printf("\n 名前 : [ %s先生 ]", &m_aName[0]);	//名前
	printf("\n 体力 : [ %d ]", m_nLife);			//体力
	printf("\n テンション : [ %d ]", m_nTension);	//テンション
}

//===================================================
//終了
//===================================================
void CTeacher::Uninit()
{

}